import numpy as np

def make_baseline_grid(type="very_complex"):

    def w(g, y1, y2, x1, x2): g[y1:y2, x1:x2] = 0
    def c(g, cy, cx, r):
        H, W = g.shape  # 这里修复了 shapes -> shape
        Y, X = np.ogrid[:H, :W]
        g[(X - cx)**2 + (Y - cy)**2 <= r**2] = 0

    W, H, res  = -1, -1, -1
    match type:
        case "simple":
            W, H, res = 120, 80, 0.1
            g = np.ones((H, W), dtype=np.uint8) * 255
            # 下面补全了漏掉的 g
            w(g, 0, 3, 0, W); w(g, H-3, H, 0, W); w(g, 0, H, 0, 3); w(g, 0, H, W-3, W)
            w(g, 3, 30, 55, 58); w(g, 42, 77, 55, 58)
            w(g, 3, 32, 80, 83); w(g, 48, 77, 80, 83)
            w(g, 95, 98, 90, 115); w(g, 95, 74, 90, 93)
            c(g, 20, 25, 2); c(g, 20, 35, 2); c(g, 28, 30, 2); c(g, 28, 40, 2)
            c(g, 65, 12, 3)
            w(g, 8, 22, 95, 112)
            
        case "complex":
            W, H, res = 200, 150, 0.1
            g = np.ones((H, W), dtype=np.uint8) * 255
            w(g, 0, 3, 0, W); w(g, H-3, H, 0, W); w(g, 0, H, 0, 3); w(g, 0, H, W-3, W)
            
            # 主走廊
            w(g, 30, 120, 40, 60)
            w(g, 30, 120, 140, 160)
            
            # 左侧房间
            w(g, 10, 30, 10, 40)
            w(g, 10, 30, 70, 100)
            w(g, 10, 30, 120, 150)
            
            # 右侧房间
            w(g, 10, 30, 170, 200)
            w(g, 10, 30, 220, 250)
            
            # 中间房间
            w(g, 50, 70, 80, 120)
            w(g, 50, 70, 180, 220)
            
            # 上方房间
            w(g, 80, 100, 50, 90)
            w(g, 80, 100, 130, 170)
            w(g, 80, 100, 210, 250)
            
            # 下方房间
            w(g, 120, 140, 70, 110)
            w(g, 120, 140, 150, 190)
            w(g, 120, 140, 230, 270)
            
            # 添加一些圆形障碍物
            c(g, 25, 25, 3); c(g, 25, 175, 3)
            c(g, 125, 25, 3); c(g, 125, 175, 3)
            c(g, 75, 75, 4); c(g, 175, 75, 4)
            c(g, 75, 125, 4); c(g, 175, 125, 4)
            
            # 添加一些不规则形状
            w(g, 60, 80, 30, 50); w(g, 60, 80, 190, 210)
            w(g, 100, 120, 30, 50); w(g, 100, 120, 190, 210)
            
        case "very_complex":
            W, H, res = 250, 200, 0.1
            g = np.ones((H, W), dtype=np.uint8) * 255

            # ============================================================
            # 1. 外墙边界
            # ============================================================
            w(g, 0, 3, 0, W); w(g, H-3, H, 0, W)
            w(g, 0, H, 0, 3); w(g, 0, H, W-3, W)

            # ============================================================
            # 2. 主干道 —— 宽窄混合
            # ============================================================
            w(g, 40, 65, 15, 235)
            w(g, 15, 185, 50, 75)
            w(g, 110, 125, 15, 235)
            w(g, 65, 135, 120, 135)

            # ============================================================
            # 3. 对角通道（45° 方向）
            # ============================================================
            for i in range(50):
                d = 3
                x = 80 + i
                y = 70 + i
                if 0 <= y < H and 0 <= x < W:
                    w(g, y, y+d, x, x+d)
                x2 = 200 - i
                y2 = 70 + i
                if 0 <= y2 < H and 0 <= x2 < W:
                    w(g, y2, y2+d, x2-3, x2)
                x3 = 85 + i
                y3 = 140 + i
                if 0 <= y3 < H and 0 <= x3 < W:
                    w(g, y3, y3+2, x3, x3+2)

            # ============================================================
            # 4. 不规则形状房间
            # ============================================================
            w(g, 145, 175, 15, 45); w(g, 160, 175, 15, 80)
            w(g, 15, 40, 150, 180); w(g, 15, 40, 175, 230)
            w(g, 80, 95, 15, 45); w(g, 95, 110, 30, 55); w(g, 110, 125, 15, 40)
            w(g, 145, 185, 155, 175); w(g, 145, 185, 210, 230); w(g, 165, 185, 155, 230)
            w(g, 155, 175, 85, 135); w(g, 145, 185, 100, 120)

            # ============================================================
            # 5. 弧形弯道（用密集圆孔模拟弧线通道）
            # ============================================================
            center_y, center_x = 130, 230
            for angle_deg in range(200, 340):
                angle = np.radians(angle_deg)
                r = 18
                cx = int(center_x + r * np.cos(angle))
                cy = int(center_y + r * np.sin(angle))
                c(g, cy, cx, 4)

            center_y2, center_x2 = 175, 100
            for angle_deg in range(-90, 30):
                angle = np.radians(angle_deg)
                r = 22
                cx = int(center_x2 + r * np.cos(angle))
                cy = int(center_y2 + r * np.sin(angle))
                c(g, cy, cx, 3)

            # ============================================================
            # 6. 圆形障碍物 + 柱子
            # ============================================================
            c(g, 50, 100, 8); c(g, 50, 160, 8); c(g, 95, 165, 6)
            for cy, cx in [(52, 130), (52, 140), (60, 130), (60, 140),
                            (120, 70), (120, 80), (130, 70), (130, 80),
                            (85, 195), (95, 195)]:
                c(g, cy, cx, 2)

            # ============================================================
            # 7. 菱形通道（通过旋转走廊近似）
            # ============================================================
            for i in range(35):
                t = i / 35.0 * np.pi * 0.5
                cx = int(160 + 25 * np.cos(t + np.pi/4))
                cy = int(85 + 25 * np.sin(t + np.pi/4))
                c(g, cy, cx, 3)

            # ============================================================
            # 8. 锥形走廊（逐渐变窄）
            # ============================================================
            for i in range(30):
                y = 25 + i
                half_w = max(2, 10 - i // 3)
                cx = 225
                w(g, y, y+1, cx - half_w, cx + half_w)

            # ============================================================
            # 9. 末端死胡同（各种朝向）
            # ============================================================
            w(g, 140, 160, 40, 48)
            w(g, 70, 80, 185, 193)
            w(g, 42, 48, 60, 80)
            w(g, 55, 61, 135, 150)

            # ============================================================
            # 10. 多通交叉路口
            # ============================================================
            c(g, 125, 50, 5)
            w(g, 125, 130, 35, 50)
            w(g, 125, 130, 50, 65)
            
        case _:
            return None, -1, -1, -1

    return g, W, H, res 
